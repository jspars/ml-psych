        <h3 class="font-extrabold">I. Confidentiality</h3>
        <p class="prose">
            As a rule, I will disclose no information about you, or the fact that you are my patient, without your
written consent. My formal Mental Health Record describes the services provided to you and contains the
dates of our sessions, your diagnosis (if upon my assessment you meet diagnostic criteria for a diagnosis),
functional status, symptoms, prognosis and progress, notes regarding professional consultations with
others regarding your care, and any psychological testing reports. Health care providers are legally
allowed to use or disclose records or information for treatment, payment, and health care operations
purposes. However, I do not routinely disclose information in such circumstances, so I will require your
permission in advance, either through your consent at the onset of our relationship (by signing the
attached general consent form), or through your written authorization at the time the need for disclosure
arises. You may revoke your permission, in writing, at any time, by contacting me.
        </p>
        <h3 class="font-extrabold">II. Limits of Confidentiality</h3>
        <p class="prose italic">
            Possible Uses and Disclosures of Mental Health Records without Consent or Authorization
        </p>

        <p class="prose">
            There are some important exceptions to this rule of confidentiality – some exceptions created voluntarily
by my own choice, and some required by law. If you wish to receive mental health services from me, you
must sign the attached form indicating that you understand and accept my policies about confidentiality
and its limits. We will discuss these issues now, but you may reopen the conversation at any time during
our work together.
        </p>

        <p class="prose">
            I may use or disclose records or other information about you without your consent or authorization in the
following circumstances, either by policy, or because legally required:
        </p>
        <ul class="my-3 prose">
            <li>
                Emergency: If you are involved in a life-threatening emergency and I cannot ask your permission,
I will share information if I believe you would have wanted me to do so, or if I believe it will be
helpful to you.
            </li>
            
            <li>
                Child Abuse Reporting: If I have reason to suspect that a child is abused or neglected, I am
required by Virginia, Pennsylvania, and Indiana law to report the matter immediately to your State
Department of Social Services.
            </li>
            <li>
                Adult Abuse Reporting: If I have reason to suspect that an elderly or incapacitated adult is abused,
neglected or exploited, I am required by Virginia, Pennsylvania, and Indiana law to immediately
make a report and provide relevant information to your State Department of Welfare or Social
Services.
            </li>
            <li>
                Endangered Person with a Mental or Physical Disability Reporting: If I have reason to suspect that
an endangered adult or person of any age who has a mental or physical disability is the victim of

battery, neglect, or exploitation, I am mandated by Indiana Law to report this to your State
Department of Disability and Rehabilitative Services or Adult Protective Services.
            </li>
            <li>
                Health Oversight: Virginia and Pennsylvania law requires that licensed psychologists report
misconduct by a health care provider of their own profession; therefore, if you share information
that leads me to believe a Virginia or Pennsylvania psychologist has engaged in professional
misconduct, I will be required to report it. By policy, I also reserve the right to report misconduct
by health care providers of other professions, if you disclose such information in therapy. By
Virginia and Pennsylvania law, if you describe unprofessional conduct by another mental health
provider of any profession, I am required to explain to you how to make such a report. If you are
yourself a health care provider, I am required by Virginia law to report to your licensing board that
you are in treatment with me if I believe your condition places the public at risk. Virginia and
Pennsylvania Licensing Boards have the power, when necessary, to subpoena relevant records in
investigating a complaint of provider incompetence or misconduct.
            </li>
            <li>
                Court Proceedings: If you are involved in a court proceeding and a request is made for information
about your diagnosis and treatment and the records thereof, such information is privileged under
Virginia, Pennsylvania, and Indiana state law, and I will not release information unless you
provide written authorization or a judge issues a court order. I recommend that we discuss the
content of your record before you decide how to proceed. If I receive a subpoena for records or
testimony, I will notify you so you can file a motion to quash (block) the subpoena, if this is what
you decide to do. However, while awaiting the judge’s decision, I am required to place said
records in a sealed envelope and provide them to the Clerk of Court.
            </li>
            <li>
                In Virginia civil court cases, therapy information is not protected by patient-therapist privilege in
child abuse cases, in cases in which your mental health is an issue, or in any case in which the
judge deems the information to be “necessary for the proper administration of justice.” In criminal
cases, Virgnia has no statute granting therapist-patient privilege, although records can sometimes
be protected on another basis.
            </li>
            <li>
                In all states in which I work, protections of privilege may not apply if I do an evaluation for a third
party or where the evaluation is court-ordered. You will be informed in advance if this is the case.
            </li>
            <li>
                Serious Threat to Health or Safety: Under Virginia, Pennsylvania, and Indiana law, if I am engaged
in my professional duties and you communicate to me a specific and immediate threat to cause
serious bodily injury or death, to an identified or to an identifiable person, and I believe you have
the intent and ability to carry out that threat immediately or imminently, I am legally required to
take steps to protect third parties. These precautions may include 1) warning the potential
victim(s), or the parent or guardian of the potential victim(s), if under 18, 2) notifying a law
enforcement officer, or 3) seeking your hospitalization. By my own policy, I may also use and
disclose medical information about you when necessary to prevent an immediate, serious threat to
your own health and safety. If you become a party in a civil commitment hearing, I can be required
to provide your records to the magistrate, your attorney or guardian ad litem, a CSB evaluator (in
Virginia), or a law enforcement officer, whether you are a minor or an adult.
            </li>
            <li>
                Workers Compensation: If you file a worker’s compensation claim, I am required by Virginia law,
upon request, to submit your relevant mental health information to you, your employer, the
insurer, or a certified rehabilitation provider.
            </li>
            <li>
                Records of Minors: Virginia, Pennsylvania, and Indiana have a number of laws that limit the
confidentiality of the records of minors. For example, parents, regardless of custody, may not be
denied access to their child’s records; and CSB evaluators (in Virginia) in civil commitment cases
have legal access to therapy records without notification or consent of parents or child. Other
circumstances may also apply, and we will discuss these in detail if I provide services to minors.
            </li>
        </ul>
        <p class="prose italic">
            Other uses and disclosures of information not covered by this notice or by the laws that apply to me will
be made only with your written permission.
        </p>

        <h3 class="font-extrabold">III. Patient’s Rights and Provider’s Duties:</h3>
        <ul class="my-3 prose">
            <li>
                Right to Request Restrictions-You have the right to request restrictions on certain uses and
disclosures of protected health information about you. You also have the right to request a limit on

the medical information I disclose about you to someone who is involved in your care or the
payment for your care. If you ask me to disclose information to another party, you may request
that I limit the information I disclose. However, I am not required to agree to a restriction you
request. To request restrictions, you must make your request in writing, and tell me: 1) what
information you want to limit; 2) whether you want to limit my use, disclosure or both; and 3) to
whom you want the limits to apply.
            </li>
            
            <li>
                Right to Receive Confidential Communications by Alternative Means and at Alternative Locations
— You have the right to request and receive confidential communications of protected health
information (PHI) by alternative means and at alternative locations. (For example, you may not
want a family member to know that you are seeing me. Upon your request, I will send your bills or
other notices to another address. You may also request that I contact you only at work, or that I do
not leave voice mail messages.) To request alternative communication, you must make your
request in writing, specifying how or where you wish to be contacted.
            </li>
            <li>
                Right to an Accounting of Disclosures – You generally have the right to receive an accounting of
disclosures of PHI for which you have neither provided consent nor authorization (as described in
section III of this Notice). On your written request, I will discuss with you the details of the
accounting process.
            </li>
            <li>
                Right to Inspect and Copy – In most cases, you have the right to inspect and copy your medical
and billing records. To do this, you must submit your request in writing. If you request a copy of
the information, I may charge a fee for costs of copying and mailing. I may deny your request to
inspect and copy if releasing such “health records would be reasonably likely to endanger the life
or physical safety of the individual or another person, or that such health record makes reference to
a person other than a health care provider and the access requested would be reasonably likely to
cause substantial harm to such referenced person.” Because these are professional records, they
may be misinterpreted and / or upsetting to untrained readers. For this reason, I recommend that
you initially review them with me. If I refuse your request for access to your records, you have a
right to have my decision reviewed by another mental health professional, which I will discuss
with you upon your request. You also have the right to request that a copy of your file be made
available to any other health care provider at your written request. I may refuse to provide you
access to certain psychotherapy notes or to information compiled in reasonable anticipation of, or
use in, a civil criminal, or administrative proceeding.
            </li>
            <li>
                Right to Amend - If you feel that protected health information I have about you is incorrect or
incomplete, you may ask me to amend the information. To request an amendment, your request
must be made in writing, and submitted to me. In addition, you must provide a reason that
supports your request. I may deny your request if you ask me to amend information that: 1) was
not created by me; I will add your request to the information record; 2) is not part of the medical
information kept by me; 3) is not part of the information which you would be permitted to inspect
and copy; 4) is accurate and complete.
            </li>
            <li>
                Right to a copy of this notice - You have the right to a paper copy of this notice. You may ask me
to give you a copy of this notice at any time. Changes to this notice: I reserve the right to change
my policies and/or to change this notice, and to make the changed notice effective for medical
information I already have about you as well as any information I receive in the future. The notice
will contain the effective date. A new copy will be given to you or posted in the waiting room. I
will have copies of the current notice available on request.
            </li>
        </ul>



        <p class="prose">
            Complaints: If you believe your privacy rights have been violated, you may file a complaint. To do this,
you must submit your request in writing to my office. You may also send a written complaint to the U.S.
Department of Health and Human Services and your State Board of Psychology.
        </p>
